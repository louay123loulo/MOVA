export const generateTimeSlots = (date) => {
  const slots = [];
  const baseHours = [8, 9, 10, 11, 14, 15, 16, 17, 18, 19, 20, 21, 22];
  
  baseHours.forEach((hour) => {
    const isBooked = Math.random() > 0.6;
    slots.push({
      id: `slot-${date}-${hour}`,
      time: `${hour.toString().padStart(2, '0')}:00`,
      endTime: `${(hour + 1).toString().padStart(2, '0')}:00`,
      isAvailable: !isBooked,
      price: hour >= 18 ? 90 : 70,
    });
  });
  
  return slots;
};
